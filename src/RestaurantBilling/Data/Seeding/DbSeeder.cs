            var seedItems = db.Items.Where(x => x.OutletId == outletSeedId).OrderBy(x => x.ItemId).Take(3).ToList();
            if (seedItems.Count < 3) return;

            if (!db.Quotations.Any(x => x.OutletId == outletSeedId))
            {
                var quote = new Quotation
                {
                    OutletId = outletSeedId,
                    QuoteNo = outletSeedId == outletId ? "QT00001" : "DQT00001",
                    QuoteDate = DateTime.UtcNow,
                    BusinessDate = bizDate,
                    SubTotal = 0m,
                    DiscountAmount = 0m,
                    TaxAmount = 0m,
                    GrandTotal = 0m,
                    Status = "Draft"
                };
                quote.Items.AddRange([
                    new QuotationItem { ItemId = seedItems[0].ItemId, ItemNameSnapshot = seedItems[0].ItemName, Qty = 1, Rate = seedItems[0].SalePrice, DiscountAmount = 0, TaxAmount = 0, LineTotal = seedItems[0].SalePrice },
                    new QuotationItem { ItemId = seedItems[1].ItemId, ItemNameSnapshot = seedItems[1].ItemName, Qty = 2, Rate = seedItems[1].SalePrice, DiscountAmount = 0, TaxAmount = 0, LineTotal = 2 * seedItems[1].SalePrice }
                ]);
                quote.SubTotal = quote.Items.Sum(x => x.Rate * x.Qty);
                quote.GrandTotal = quote.SubTotal;
                db.Quotations.Add(quote);
            }

            Bill? paidBill = null;
            if (!db.Bills.Any(x => x.OutletId == outletSeedId))
            {
                var billNo = outletSeedId == outletId ? "TI000001" : "DTI000001";
                var bill = new Bill(outletSeedId, billNo, bizDate, BillType.DineIn);
                bill.AddItem(new BillItem(seedItems[0].ItemId, seedItems[0].ItemName, 1, seedItems[0].SalePrice, 0m, 0m));
                bill.AddItem(new BillItem(seedItems[1].ItemId, seedItems[1].ItemName, 1, seedItems[1].SalePrice, 0m, 0m));
                bill.Settle([new Payment(PaymentMode.Cash, bill.GrandTotal, referenceNo: $"{billNo}-PAY")]);
                db.Bills.Add(bill);
                await db.SaveChangesAsync();
                paidBill = bill;
            }
            else
            {
                paidBill = db.Bills
                    .Where(x => x.OutletId == outletSeedId)
                    .OrderByDescending(x => x.BillId)
                    .FirstOrDefault();
            }

            if (!db.KotHeaders.Any(x => x.OutletId == outletSeedId) && paidBill is not null)
            {
